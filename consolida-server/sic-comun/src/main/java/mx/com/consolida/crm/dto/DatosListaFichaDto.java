package mx.com.consolida.crm.dto;

public class DatosListaFichaDto {

	private String grupo;
	private String subgrupo;
	private String accionista;
	private Long porcentaje;
	private String nombreDocumento;
	private String descripcionDefinicion;
	private Long idCMS;
	private String nombreOdescripcion;
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public String getSubgrupo() {
		return subgrupo;
	}
	public void setSubgrupo(String subgrupo) {
		this.subgrupo = subgrupo;
	}
	public String getAccionista() {
		return accionista;
	}
	public void setAccionista(String accionista) {
		this.accionista = accionista;
	}
	public Long getPorcentaje() {
		return porcentaje;
	}
	public void setPorcentaje(Long porcentaje) {
		this.porcentaje = porcentaje;
	}
	public String getNombreDocumento() {
		return nombreDocumento;
	}
	public void setNombreDocumento(String nombreDocumento) {
		this.nombreDocumento = nombreDocumento;
	}
	public String getDescripcionDefinicion() {
		return descripcionDefinicion;
	}
	public void setDescripcionDefinicion(String descripcionDefinicion) {
		this.descripcionDefinicion = descripcionDefinicion;
	}
	public Long getIdCMS() {
		return idCMS;
	}
	public void setIdCMS(Long idCMS) {
		this.idCMS = idCMS;
	}
	public String getNombreOdescripcion() {
		return nombreOdescripcion;
	}
	public void setNombreOdescripcion(String nombreOdescripcion) {
		this.nombreOdescripcion = nombreOdescripcion;
	}
	
	
}

